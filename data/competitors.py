def calc_monthly_cost(competitor_key: str, num_techs: int) -> float | None:
    """Calculate estimated monthly cost for a competitor at a given team size."""
    comp = DIRECT_COMPETITORS.get(competitor_key)
    if not comp:
        return None

    model = comp.get("pricing_model", "")
    plans = comp.get("plans", [])

    if not plans:
        return None

    if model == "flat_unlimited_users":
        return min(p["base"] for p in plans if p.get("base") is not None)

    if model in ("per_user", "base_plus_per_user"):
        best = float("inf")
        for p in plans:
            base = p.get("base")
            per_user = p.get("per_user", 0)
            included = p.get("included", 1) or 1
            if base is None:
                continue
            extra = max(0, num_techs - included)
            cost = base + extra * per_user
            best = min(best, cost)
        return best if best != float("inf") else None

    if model == "tiered_flat":
        eligible = []
        for p in plans:
            base = p.get("base")
            max_u = p.get("max_users") or 9999
            per_user = p.get("per_user", 0)
            included = p.get("included") or max_u
            if base is None:
                continue
            if num_techs > max_u:
                continue  # team exceeds this plan's hard user cap
            cost = base + max(0, num_techs - included) * per_user
            eligible.append(cost)
        return min(eligible) if eligible else None

    if model == "tiered_enterprise":
        base = min((p["base"] for p in plans if p.get("base")), default=None)
        return base

    return None


